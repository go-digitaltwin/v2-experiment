package deltagen

import (
	"bytes"
	"fmt"
	"go/format"
	"go/types"
	"io"
	"strings"
	"unicode"

	"golang.org/x/tools/go/packages"
)

// FieldKind classifies a struct field as either part of the primary key or a
// mutable property.
type FieldKind int

const (
	FieldProperty FieldKind = iota
	FieldKey
)

// FieldInfo describes a single struct field.
type FieldInfo struct {
	Name    string
	Kind    FieldKind
	TypeStr string // qualified type string (e.g. "netip.Addr", "[]string")
	PkgPath string // import path for non-builtin types ("net/netip"), empty otherwise
}

// TypeInfo carries the parsed metadata for the target struct.
type TypeInfo struct {
	PkgName  string
	TypeName string
	Fields   []FieldInfo
}

// Keys returns only the primary-key fields.
func (ti TypeInfo) Keys() []FieldInfo {
	var out []FieldInfo
	for _, f := range ti.Fields {
		if f.Kind == FieldKey {
			out = append(out, f)
		}
	}
	return out
}

// Properties returns only the non-key fields.
func (ti TypeInfo) Properties() []FieldInfo {
	var out []FieldInfo
	for _, f := range ti.Fields {
		if f.Kind == FieldProperty {
			out = append(out, f)
		}
	}
	return out
}

// Config carries the inputs for the generator.
type Config struct {
	TypeName string   // -type value
	Keys     []string // -key value, split by comma
	Dir      string   // package directory (from working dir for now)
}

// Generate loads the target struct from Dir, classifies its fields, and
// returns the formatted generated source.
func Generate(cfg Config) ([]byte, error) {
	ti, err := loadType(cfg)
	if err != nil {
		return nil, err
	}

	var buf bytes.Buffer
	emit(&buf, ti)

	return format.Source(buf.Bytes())
}

func loadType(cfg Config) (TypeInfo, error) {
	pkgCfg := &packages.Config{
		Mode: packages.NeedName | packages.NeedTypes | packages.NeedSyntax,
		Dir:  cfg.Dir,
	}
	pkgs, err := packages.Load(pkgCfg, ".")
	if err != nil {
		return TypeInfo{}, fmt.Errorf("loading package: %w", err)
	}
	if len(pkgs) == 0 {
		return TypeInfo{}, fmt.Errorf("no packages found in %s", cfg.Dir)
	}
	pkg := pkgs[0]
	if len(pkg.Errors) > 0 {
		return TypeInfo{}, fmt.Errorf("package errors: %v", pkg.Errors)
	}

	obj := pkg.Types.Scope().Lookup(cfg.TypeName)
	if obj == nil {
		return TypeInfo{}, fmt.Errorf("type %s not found in package %s", cfg.TypeName, pkg.Name)
	}
	named, ok := obj.Type().(*types.Named)
	if !ok {
		return TypeInfo{}, fmt.Errorf("%s is not a named type", cfg.TypeName)
	}
	st, ok := named.Underlying().(*types.Struct)
	if !ok {
		return TypeInfo{}, fmt.Errorf("%s is not a struct type", cfg.TypeName)
	}

	keySet := make(map[string]bool, len(cfg.Keys))
	for _, k := range cfg.Keys {
		keySet[k] = true
	}

	ti := TypeInfo{
		PkgName:  pkg.Name,
		TypeName: cfg.TypeName,
	}
	for i := range st.NumFields() {
		field := st.Field(i)
		if !field.Exported() {
			continue
		}
		kind := FieldProperty
		if keySet[field.Name()] {
			kind = FieldKey
		}
		typeStr, pkgPath := qualifiedType(field.Type(), pkg.Types)
		ti.Fields = append(ti.Fields, FieldInfo{
			Name:    field.Name(),
			Kind:    kind,
			TypeStr: typeStr,
			PkgPath: pkgPath,
		})
	}

	// Validate all declared keys were found.
	for _, k := range cfg.Keys {
		found := false
		for _, f := range ti.Fields {
			if f.Name == k && f.Kind == FieldKey {
				found = true
				break
			}
		}
		if !found {
			return TypeInfo{}, fmt.Errorf("key field %q not found in struct %s", k, cfg.TypeName)
		}
	}

	return ti, nil
}

// qualifiedType returns the type string suitable for generated code and the
// import path of the package if the type requires one.
func qualifiedType(t types.Type, localPkg *types.Package) (typeStr string, pkgPath string) {
	switch t := t.(type) {
	case *types.Named:
		obj := t.Obj()
		if obj.Pkg() == nil || obj.Pkg() == localPkg {
			return obj.Name(), ""
		}
		return obj.Pkg().Name() + "." + obj.Name(), obj.Pkg().Path()
	case *types.Slice:
		elemStr, elemPkg := qualifiedType(t.Elem(), localPkg)
		return "[]" + elemStr, elemPkg
	case *types.Pointer:
		elemStr, elemPkg := qualifiedType(t.Elem(), localPkg)
		return "*" + elemStr, elemPkg
	case *types.Basic:
		return t.Name(), ""
	default:
		return types.TypeString(t, nil), ""
	}
}

func emit(w io.Writer, ti TypeInfo) {
	fmt.Fprintln(w, "// Code generated by gen-delta-builder; DO NOT EDIT.")
	fmt.Fprintln(w)

	imports := collectImports(ti)
	if len(imports) > 0 {
		fmt.Fprintf(w, "package %s\n\n", ti.PkgName)
		fmt.Fprintln(w, "import (")
		for _, imp := range imports {
			fmt.Fprintf(w, "\t%q\n", imp)
		}
		fmt.Fprintln(w, ")")
	} else {
		fmt.Fprintf(w, "package %s\n", ti.PkgName)
	}

	emitTypeAndConstructor(w, ti)
	emitSetMethods(w, ti)
}

func collectImports(ti TypeInfo) []string {
	seen := make(map[string]bool)
	var imports []string
	for _, f := range ti.Properties() {
		if f.PkgPath != "" && !seen[f.PkgPath] {
			seen[f.PkgPath] = true
			imports = append(imports, f.PkgPath)
		}
	}
	return imports
}

func emitSetMethods(w io.Writer, ti TypeInfo) {
	deltaName := ti.TypeName + "Delta"
	for _, f := range ti.Properties() {
		fmt.Fprintln(w)
		fmt.Fprintf(w, "// Set%s asserts the [%s.%s] value.\n", f.Name, ti.TypeName, f.Name)
		fmt.Fprintf(w, "func (d *%s) Set%s(v %s) {}\n", deltaName, f.Name, f.TypeStr)
	}
}

func emitTypeAndConstructor(w io.Writer, ti TypeInfo) {
	deltaName := ti.TypeName + "Delta"
	keys := ti.Keys()

	keyLinks := make([]string, len(keys))
	for i, k := range keys {
		keyLinks[i] = fmt.Sprintf("[%s.%s]", ti.TypeName, k.Name)
	}

	props := ti.Properties()

	// Type doc comment.
	fmt.Fprintln(w)
	if len(props) > 0 {
		fmt.Fprintf(w, "// %s builds a partial update instruction for\n", deltaName)
		fmt.Fprintf(w, "// [%s].\n", ti.TypeName)
		fmt.Fprintln(w, "//")
		fmt.Fprintln(w, "// Each property has three builder methods: Set asserts a value, Clear")
		fmt.Fprintln(w, "// retracts it, and Skip (the zero-state default) leaves it unaffected.")
		fmt.Fprintln(w, "//")
		fmt.Fprintf(w, "// Create with [New%s] using the entity's primary key\n", deltaName)
		fmt.Fprintf(w, "// (%s).\n", strings.Join(keyLinks, ", "))
	} else {
		fmt.Fprintf(w, "// %s builds a partial update instruction for\n", deltaName)
		fmt.Fprintf(w, "// [%s].\n", ti.TypeName)
		fmt.Fprintln(w, "//")
		fmt.Fprintf(w, "// Create with [New%s] using the entity's primary key\n", deltaName)
		fmt.Fprintf(w, "// (%s).\n", strings.Join(keyLinks, ", "))
	}

	// Struct with unexported PK fields.
	fmt.Fprintf(w, "type %s struct {\n", deltaName)
	for _, k := range keys {
		fmt.Fprintf(w, "\t%s %s\n", lowerFirst(k.Name), k.TypeStr)
	}
	fmt.Fprintln(w, "\t// contains filtered or unexported fields")
	fmt.Fprintln(w, "}")

	// Constructor.
	fmt.Fprintln(w)
	if len(keys) == 1 {
		fmt.Fprintf(w, "// New%s returns a delta targeting the [%s]\n", deltaName, ti.TypeName)
		fmt.Fprintf(w, "// identified by its %s.\n", keyLinks[0])
	} else {
		fmt.Fprintf(w, "// New%s returns a delta targeting the [%s] identified\n", deltaName, ti.TypeName)
		fmt.Fprintf(w, "// by its composite key (%s).\n", strings.Join(keyLinks, ", "))
	}

	params := make([]string, len(keys))
	for i, k := range keys {
		params[i] = fmt.Sprintf("%s %s", lowerFirst(k.Name), k.TypeStr)
	}
	fmt.Fprintf(w, "func New%s(%s) *%s {\n", deltaName, strings.Join(params, ", "), deltaName)
	assignments := make([]string, len(keys))
	for i, k := range keys {
		assignments[i] = fmt.Sprintf("%s: %s", lowerFirst(k.Name), lowerFirst(k.Name))
	}
	fmt.Fprintf(w, "\treturn &%s{%s}\n", deltaName, strings.Join(assignments, ", "))
	fmt.Fprintln(w, "}")

	// PK getters.
	for i, k := range keys {
		fmt.Fprintln(w)
		if len(keys) == 1 {
			fmt.Fprintf(w, "// %s returns the primary key (%s) of the target entity.\n", k.Name, keyLinks[i])
		} else {
			fmt.Fprintf(w, "// %s returns the %s component of the primary key.\n", k.Name, keyLinks[i])
		}
		fmt.Fprintf(w, "func (d *%s) %s() %s { return d.%s }\n", deltaName, k.Name, k.TypeStr, lowerFirst(k.Name))
	}
}

// lowerFirst converts the first character of s to lower case.
func lowerFirst(s string) string {
	if s == "" {
		return s
	}
	runes := []rune(s)
	// Handle all-uppercase abbreviations like "IMEI" -> "imei"
	if len(runes) > 1 && unicode.IsUpper(runes[1]) {
		return strings.ToLower(s)
	}
	runes[0] = unicode.ToLower(runes[0])
	return string(runes)
}
