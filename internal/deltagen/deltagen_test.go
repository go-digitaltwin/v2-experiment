package deltagen_test

import (
	"flag"
	"fmt"
	"os"
	"path/filepath"
	"strings"
	"testing"

	"github.com/google/go-cmp/cmp"

	"github.com/go-digitaltwin/v2-experiment/internal/deltagen"
)

var update = flag.Bool("update", false, "update golden files")

func TestGenerate(t *testing.T) {
	tests := []struct {
		name   string
		dir    string
		typ    string
		keys   []string
		apply  bool
		golden string
	}{
		{
			name:   "simple",
			dir:    "testdata/simple",
			typ:    "Device",
			keys:   []string{"ID"},
			golden: "testdata/simple/simple.golden",
		},
		{
			name:   "composite",
			dir:    "testdata/composite",
			typ:    "Connection",
			keys:   []string{"TenantID", "Name"},
			golden: "testdata/composite/composite.golden",
		},
		{
			name:   "keyonly",
			dir:    "testdata/keyonly",
			typ:    "Label",
			keys:   []string{"Name"},
			golden: "testdata/keyonly/keyonly.golden",
		},
		{
			name:   "simple_apply",
			dir:    "testdata/simple",
			typ:    "Device",
			keys:   []string{"ID"},
			apply:  true,
			golden: "testdata/simple/simple_apply.golden",
		},
		{
			name:   "composite_apply",
			dir:    "testdata/composite",
			typ:    "Connection",
			keys:   []string{"TenantID", "Name"},
			apply:  true,
			golden: "testdata/composite/composite_apply.golden",
		},
		{
			name:   "keyonly_apply",
			dir:    "testdata/keyonly",
			typ:    "Label",
			keys:   []string{"Name"},
			apply:  true,
			golden: "testdata/keyonly/keyonly_apply.golden",
		},

		// Coverage exercises type diversity across the testdata/coverage
		// package. Each scenario tests a Direct type (plain fields) and a
		// Containers type (maps, slices, arrays of the same field types).
		{
			name:   "Coverage/Builtin/Containers",
			dir:    "testdata/coverage",
			typ:    "Metric",
			keys:   []string{"ID"},
			golden: "testdata/coverage/metric.golden",
		},
		{
			name:   "Coverage/Stdlib/Direct",
			dir:    "testdata/coverage",
			typ:    "Session",
			keys:   []string{"ID"},
			golden: "testdata/coverage/session.golden",
		},
		{
			name:   "Coverage/Stdlib/Containers",
			dir:    "testdata/coverage",
			typ:    "SessionContainers",
			keys:   []string{"ID"},
			golden: "testdata/coverage/session_containers.golden",
		},
		{
			name:   "Coverage/Subpackage/Direct",
			dir:    "testdata/coverage",
			typ:    "Endpoint",
			keys:   []string{"ID"},
			golden: "testdata/coverage/endpoint.golden",
		},
		{
			name:   "Coverage/Subpackage/Containers",
			dir:    "testdata/coverage",
			typ:    "EndpointContainers",
			keys:   []string{"ID"},
			golden: "testdata/coverage/endpoint_containers.golden",
		},
		{
			name:   "Coverage/SamePackage/Direct",
			dir:    "testdata/coverage",
			typ:    "Task",
			keys:   []string{"ID"},
			golden: "testdata/coverage/task.golden",
		},
		{
			name:   "Coverage/SamePackage/Containers",
			dir:    "testdata/coverage",
			typ:    "TaskContainers",
			keys:   []string{"ID"},
			golden: "testdata/coverage/task_containers.golden",
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			cfg := deltagen.Config{
				TypeName: tt.typ,
				Keys:     tt.keys,
				Dir:      tt.dir,
				Apply:    tt.apply,
				Command:  testCommand(tt.typ, tt.keys, tt.apply),
			}
			got, err := deltagen.Generate(cfg)
			if err != nil {
				t.Fatalf("Generate: %v", err)
			}

			goldenPath, _ := filepath.Abs(tt.golden)
			if *update {
				if err := os.WriteFile(goldenPath, got, 0o644); err != nil {
					t.Fatalf("updating golden file: %v", err)
				}
				return
			}

			want, err := os.ReadFile(goldenPath)
			if err != nil {
				t.Fatalf("reading golden file: %v", err)
			}

			if diff := cmp.Diff(string(want), string(got)); diff != "" {
				actualPath := tt.golden[:len(tt.golden)-len(".golden")] + ".actual"
				_ = os.WriteFile(actualPath, got, 0o644)
				t.Errorf("output mismatch (-want +got, wrote %s):\n%s", actualPath, diff)
			}
		})
	}
}

// testCommand builds the "Code generated by ..." header string that the
// golden files expect. Changing the argument format here or editing the
// header line in a golden file requires updating the other to match.
func testCommand(typ string, keys []string, apply bool) string {
	cmd := fmt.Sprintf("gen-delta-builder -type %s -key %s", typ, strings.Join(keys, ","))
	if apply {
		cmd += " -apply"
	}
	return cmd
}
